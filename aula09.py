# SUBTRACAO
subtracao = 10 - 5
print('-> Subtração =', subtracao)

# MULTIPLICACAO
multiplicacao = 10 * 10
print('-> Multiplicação =', multiplicacao)

# DIVISAO (Sempre retorna FLOAT)
divisao = 10 / 2
print('-> Divisão =', divisao)

# DIVISAO INTEIRA
divisa_inteira = 10 // 3
print('-> Divisão inteira =', divisa_inteira)

# EXPONENCIACAO
exponenciacao = 3 ** 2
print('-> Exponenciação =', exponenciacao)

# MODULO (RESTO DA DIVISAO)
modulo = 55 % 2
print('-> Módulo =', modulo)