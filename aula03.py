"""
Python = Linguagem de programação
Tipo de tipagem = Dinâmica / Forte (Python ja sabe o tipo sem precisarmos informar)
str -> string -> texto
Strings são textos que estão dentro de aspas
"""
print(1234)

# Aspas simples
print('Manuela')

# Aspas duplas
print("Kézya Medeiros")

# Escape
print("Kézya \"Medeiros\"")

# r -> Exibe o caractere de escape
print(r"Kézya \"Medeiros\"")

# Outra maneira de exibir o caractere
print(1, '"Kézya "Medeiros"')
print(2, "Kézya 'Medeiros'")
