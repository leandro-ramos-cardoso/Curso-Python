# Tipos int e float
# int -> Número inteiro (Qualquer número positivo ou negativo que não tenha casa decimal)
print(11) # int
print(-11) # int
print(0)

# TIPOS PRIMITIVOS
#string, int, float, boolean

# float -> Número com ponto flutuante (Qualquer número positivo ou negativo que tenha casa decimal)
# float sem sinal é considerado positivo.
print(1.1, 10.11)
print(0.0, -1.5)

# A função type mostra o tipo que o Python inferiu ao valor.
print(type('Manuela'))
print(type(1))
print(type(-1))
print(type(-1.1), type(1.1), type(0.0))